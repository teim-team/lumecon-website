/* Deterministic slug per scene, derived from the chip. Used by the
   hero (URL hash) and by the static demo pages (route param). */
export const slugify = (s: string): string => s
  .toLowerCase()
  .replace(/['’]/g, '')
  .replace(/[^a-z0-9]+/g, '-')
  .replace(/^-+|-+$/g, '')
  .slice(0, 60);

export const sceneSlug = (s: Scene): string => slugify(s.chip);
